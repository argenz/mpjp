package s026;

public class Assignment {
    public static void main(String[] args) {
        int alpha = 2;

        alpha += 8; // 10 // alpha = alpha + 8 ;
        System.out.println(alpha);

        alpha -= 3; // 7  // alpha = alpha - 3 ;
        System.out.println(alpha);

        alpha *= 2; // 14 // alpha = alpha*2 ;
        System.out.println(alpha);

        alpha /= 2; // 7  // alpha = alpha/2 ;
        System.out.println(alpha);

        alpha %= 5; // 2  // alpha = alpha%5 ;
        System.out.println(alpha);
    }
}
